/// Type of animation for selected icons
enum IconAnimationType {
  /// Scales the icon up
  scale,

  /// Rotates the icon
  rotate,

  /// Slides the icon up
  slide,

  /// Fades the icon in/out
  fade,

  /// Flips the icon in 3D space
  flip,

  /// Bouncy elastic jump
  bounce,

  /// Intense glowing pulse with proximity attraction
  magnetic,
}
